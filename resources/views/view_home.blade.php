        Jalan Sudirman, atau lebih tepatnya di tengah kota. Hal ini menjadi penting sebagai akses literasi bagi masyarakat
        Kota Pekanbaru. Pustaka ini ramai dikunjungi oleh pelajar dan mahasiswa setiap harinya. Pustaka ini juga sering 
        mengadakan gelaran literasi di akhir pekan.

